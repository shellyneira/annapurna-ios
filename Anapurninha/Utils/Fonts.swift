import SwiftUI

extension Font {
    
    static func poppinsSB(size: CGFloat) -> Font {
        return Font.custom("Poppins-SemiBold", size: size)
    }
    
    static func poppinsB(size: CGFloat) -> Font {
        return Font.custom("Poppins-Bold", size: size)
    }
}
